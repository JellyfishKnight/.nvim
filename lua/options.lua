-- ==================== 选项配置模块 ====================
-- 创建 vim.opt 的局部引用，提高代码可读性和性能
local opt = vim.opt

-- ==================== 行号显示设置 ====================
-- 显示绝对行号（左侧显示 1, 2, 3, 4...）
-- 用途：快速定位到特定行，查看错误信息中的行号
opt.number = true

-- 显示相对行号（当前行显示 0，上下行显示相对距离）
-- 用途：快速移动，例如 "5j" 向下移动5行，"3k" 向上移动3行
-- 同时启用 number 和 relativenumber 会在当前行显示绝对行号，其他行显示相对行号
opt.relativenumber = true

-- ==================== 缩进和制表符设置 ====================
-- 设置 Tab 键的显示宽度为 4 个空格
-- 当文件中有制表符时，显示为 4 个空格的宽度
opt.tabstop = 4

-- 设置自动缩进时使用的空格数为 4
-- 影响 >>, <<, 以及自动缩进的行为
opt.shiftwidth = 4

-- 将 Tab 键转换为空格
-- true: 按 Tab 键时插入空格而不是制表符
-- false: 按 Tab 键时插入真正的制表符
-- 建议设为 true，避免不同编辑器显示不一致的问题
opt.expandtab = true

-- ==================== 搜索行为设置 ====================
-- 搜索时忽略大小写
-- 例如：搜索 "hello" 时也会匹配 "Hello", "HELLO", "HeLLo"
opt.ignorecase = true

-- 智能大小写搜索
-- 如果搜索词包含大写字母，则变为大小写敏感
-- 例如：搜索 "hello" 忽略大小写，搜索 "Hello" 则精确匹配大小写
-- 这个选项只有在 ignorecase = true 时才有效
opt.smartcase = true

-- ==================== 用户交互设置 ====================
-- 启用鼠标支持
-- "a" = 在所有模式下启用鼠标（普通、插入、可视、选择、命令行模式）
-- 用途：可以用鼠标点击定位光标、选择文本、调整窗口大小等
opt.mouse = "a"

-- ==================== 其他推荐设置 ====================
-- 以下是一些额外的推荐设置，可以改善使用体验

-- 启用真彩色支持（24位颜色）
-- 让主题颜色显示更准确
opt.termguicolors = true

-- 始终显示标志列（左侧用于显示错误、警告、Git状态等标记的列）
-- "yes" = 总是显示，避免文本跳动
-- "auto" = 根据需要显示/隐藏
opt.signcolumn = "yes"

-- 设置光标上下保持的最小行数
-- 当光标接近屏幕顶部/底部时，自动滚动保持 8 行的缓冲区
opt.scrolloff = 8

-- 水平滚动时的缓冲区
opt.sidescrolloff = 8

-- 不自动换行显示长行
-- false = 长行会延伸到屏幕右侧
-- true = 长行会自动换行显示
opt.wrap = false

-- 分割窗口的默认行为
opt.splitright = true    -- 垂直分割时新窗口在右侧
opt.splitbelow = true    -- 水平分割时新窗口在下方

-- 使用系统剪贴板
-- 让 Neovim 的复制粘贴与系统剪贴板同步
opt.clipboard = "unnamedplus"

-- 启用持久化撤销
-- 关闭文件后重新打开仍能撤销之前的操作
opt.undofile = true

-- 更新时间（毫秒）
-- 影响自动保存、CursorHold 事件等的触发频率
opt.updatetime = 250

-- 快捷键等待时间（毫秒）
-- 例如按下 leader 键后，等待 300ms 来输入下一个键
opt.timeoutlen = 300
